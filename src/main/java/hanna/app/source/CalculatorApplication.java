package hanna.app.source;

public class CalculatorApplication {

    public int addNumbers(int one, int two) {
        return one + two;
    }

    public int subtractNumbers(int one, int two) {
        return one - two;
    }

    public int multiplyNumbers(int one, int two) {
        return one * two;
    }

    public int getQuotientByDividingNumbers(int one, int two) {
        return one / two;
    }

    public int getReminderByDividingNumbers(int one, int two) {
        return one % two;
    }

}
